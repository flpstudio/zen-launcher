// ============ Language Settings UI ============

const LANG_FLAGS = {
  en: '🇺🇸', es: '🇪🇸', fr: '🇫🇷', de: '🇩🇪', it: '🇮🇹',
  pt: '🇧🇷', ru: '🇷🇺', ja: '🇯🇵', ko: '🇰🇷', zh: '🇨🇳'
};

function updateLangBtnFlag() {
  const flagEl = document.getElementById('langBtnFlag');
  if (flagEl) flagEl.textContent = LANG_FLAGS[currentLanguage] || '🌐';
}

function initLanguageSettings() {
  const langBtn = document.getElementById('settingsLangBtn');
  const langPicker = document.getElementById('settingsLangPicker');

  // Set initial flag
  updateLangBtnFlag();

  // Toggle picker open/close
  if (langBtn && langPicker) {
    langBtn.addEventListener('click', () => {
      const isOpen = langPicker.classList.toggle('open');
      langBtn.classList.toggle('active', isOpen);
    });
  }

  // Mark current language active
  document.querySelectorAll('.lang-picker-item').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLanguage);
    btn.addEventListener('click', () => {
      const lang = btn.dataset.lang;
      
      // Close picker immediately
      if (langPicker) langPicker.classList.remove('open');
      if (langBtn) langBtn.classList.remove('active');
      
      // Skip updates if same language
      if (lang === currentLanguage) return;
      
      saveLanguage(lang);
      applyTranslations();
      updateLangBtnFlag();

      // Re-render widgets that have dynamic content
      updateClockDateArea();
      renderWeather(lastWeatherData, lastCityName);
      renderMeetings(lastMeetingsData);
    });
  });
}

