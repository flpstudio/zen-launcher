let currentLanguage = 'en';
const LANGUAGE_STORAGE_KEY = 'appLanguage';

// Get browser language
function getBrowserLanguage() {
  const lang = (navigator.language || navigator.userLanguage || 'en').toLowerCase();
  const supported = ['en', 'es', 'fr', 'de', 'it', 'pt', 'ru', 'ja', 'ko', 'zh'];
  if (supported.includes(lang)) return lang;
  const prefix = lang.split('-')[0];
  if (supported.includes(prefix)) return prefix;
  return 'en';
}

// Load saved language or detect from browser
async function loadLanguage() {
  return new Promise((resolve) => {
    chrome.storage.local.get([LANGUAGE_STORAGE_KEY], (result) => {
      if (result[LANGUAGE_STORAGE_KEY]) {
        currentLanguage = result[LANGUAGE_STORAGE_KEY];
      } else {
        currentLanguage = getBrowserLanguage();
      }
      resolve(currentLanguage);
    });
  });
}

// Save language preference
function saveLanguage(lang) {
  currentLanguage = lang;
  chrome.storage.local.set({ [LANGUAGE_STORAGE_KEY]: lang });
}

// Get translation
function t(key) {
  return TRANSLATIONS[currentLanguage]?.[key] || TRANSLATIONS['en'][key] || key;
}

// Get month name
function getMonthName(index, short = false) {
  const key = short ? 'shortMonths' : 'months';
  return TRANSLATIONS[currentLanguage]?.[key]?.[index] || TRANSLATIONS['en'][key][index];
}

// Apply translations to elements with data-i18n attribute
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
  
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.title = t(key);
  });
  
  // Translate select options
  document.querySelectorAll('[data-i18n-option]').forEach(el => {
    const key = el.getAttribute('data-i18n-option');
    el.textContent = t(key);
  });
  
  // Update language picker active state & button flag
  document.querySelectorAll('.lang-picker-item').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLanguage);
  });
  if (typeof updateLangBtnFlag === 'function') updateLangBtnFlag();

  // Update onboarding hint (has complex HTML structure)
  if (typeof updateOnboardingText === 'function') {
    updateOnboardingText();
  }
}

